
public class ActividadesCadenas {

	public static void main(String[] args) {

		// Método length()
		String palabra = "león";
		System.out.println("La palabra " + palabra + " tiene " 
				+ palabra.length() + " caracteres.");

		// Método charAt()
		System.out.println("La primera letra de la palabra " + palabra 
				+ " es " + palabra.charAt(0) + ".");
		
	}

}
